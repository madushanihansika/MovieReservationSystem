    private void reserveSeat(JLabel lbl) {
        try {
            SeatDTO seatDTO = SeatController.findById(lbl.getText());

            if (seatDTO != null) {
                seatDTO.setIsbooked(true);
                boolean isUpdate = SeatController.updateSeat(seatDTO);

                if (isUpdate) {
                    bookedSeat();
                    txtSeatPrice.setText(seatDTO.getSeatPrice() + "");
                    totalPrice = totalPrice + Double.parseDouble(txtSeatPrice.getText());
                    txtTotal.setText(totalPrice + "");
                }
            }

        } catch (Exception ex) {
            Logger.getLogger(BookingForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

