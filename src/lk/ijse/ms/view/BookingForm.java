    private void getMovieTime(String movieName) {
        try {
            cmbTime.removeAllItems();
            MovieDetailDTO mddto = new MovieDetailDTO();
            ScheduleDTO sdto = new ScheduleDTO();
            ThereatDTO thereatDTO = new ThereatDTO();
            SeatDTO seatDTO = new SeatDTO();
            List<ThereatDTO> thereatDTOs = ThereatController.getAllThereat();
            List<SeatDTO> sdtos = SeatController.getAllSeat();

            try {
                List<MovieDetailDTO> detailDTOs = MovieDetailController.getAllMovieDetail();
                for (MovieDetailDTO detailDTO : detailDTOs) {
                    if (detailDTO.getMovieName().equals(movieName)) {
                        mddto = detailDTO;
                        sdto = detailDTO.getScheduleDTO();

                        txtLanguage.setText(mddto.getLanguage());

                        thereatDTO = detailDTO.getThereatDTO();

                        if (mddto.getScheduleDTO().getScheduleId().equals(sdto.getScheduleId())) {
                            cmbTime.addItem(sdto.getScheduleTime());
                            for (ThereatDTO tdto : thereatDTOs) {
                                if (tdto.getThereatId().equals(thereatDTO.getThereatId())) {
                                    txtThereadName.setText(tdto.getThereatName());
                                    for (SeatDTO sDTO : sdtos) {
                                        System.out.println(sDTO.getTheatrDTO() == null);
                                        if (tdto.getThereatId().equals(sDTO.getTheatrDTO().getThereatId())) {
                                            cmbMovieType.addItem(sDTO.getSeattype());
                                        }
                                    }
                                }
                            }
                        }
                    }
                    //cmbTime.addItem(dTO.getScheduleDTO().getScheduleTime());
                }
            } catch (Exception ex) {
                Logger.getLogger(BookingForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (Exception ex) {
            Logger.getLogger(BookingForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

